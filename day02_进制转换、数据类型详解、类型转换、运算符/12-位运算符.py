# 按位与 & :都为1为1
# 按位或 | ：一个为1为1
# 按位异或 ^ ：相同为1不同为0
# 按位左移 << ：右边补0(移几位补几位)
# 按位右移 >> ：右边补0(移几位补几位)
# 按位取反 ~ ：略
# 转换为二进制的基础计算
a = 10
b = 13

print(a & b)  # 8

print(a << 3)  # a<<n ==> a*2的n次方

y = 15

print(y >> 2)  # y>>n ==>y除以2的n次方


