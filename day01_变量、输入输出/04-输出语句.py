# python 里使用print 语句输出语句
# print(value, ..., sep=' ', end='\n', file=sys.stdout, flush=False)
# sep: 多个值的分隔符，默认空格
# end: 执行完一个print 后，接下来要输出的字符。默认\n 表示换行
# file: 输出到文件

print('hello', end='我要换行了\n')
print('hello', 'tom', 'hahah', sep='+')
