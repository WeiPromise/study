# math 与数学相关的模块

import math

# 求阶层
print(math.factorial(5))

# 向下取整
print(math.floor(12.34))

# 向上取整
print(math.ceil(12.34))

# 幂函数
print(math.pow(2,6))

# 四舍五入没有，是一个内置函数

# 三角函数
print(math.sin(math.pi/6)) # 30
print(math.cos(math.pi/3)) # 60
print(math.tan(math.pi/4)) # 45


